#Crear un diccionario con informacion sobre una persona
persona = {
    "Nombre" : "Ana" ,
    "Edad" : 18,
    "Profesion" : "Ingeniera",
    "Ciudad" : "Monterrey"
}

# Acceder a los valores del diccionario
Nombre = persona["Nombre"]
Edad = persona["Edad"]
Profesion = persona["Profesion"]
Ciudad = persona["Ciudad"]

#Imprimir la informacion de la persona
print("Nombre:", Nombre)
print("Edad:", Edad)
print("Profesion:", Profesion)
print("Ciudad:", Ciudad)